import "reflect-metadata";
import { Container } from "inversify";
import dotenv from 'dotenv';
import NodemailerEmailService from "./services/NodemailerEmailService.js";
import EmailNotificationStrategy from "./services/EmailNotificationStrategy.js";
import UINotificationStrategy from "./services/UINotificationStrategy.js";
import NotificationContext from "./services/NotificationContext.js";
import OTPService from "./services/OTPService.js";
import NotificationService from "./services/NotificationService.js";
import AuthController from "./controllers/AuthController.js";
import TicketController from "./controllers/TicketController.js";
import { TYPES } from "./types.js";
dotenv.config();
const container = new Container();
container.bind(TYPES.IEmailService).to(NodemailerEmailService);
container.bind(TYPES.EmailNotificationStrategy).to(EmailNotificationStrategy);
container.bind(TYPES.UINotificationStrategy).to(UINotificationStrategy);
container.bind(TYPES.NotificationContext).to(NotificationContext);
container.bind(TYPES.OTPService).to(OTPService);
container.bind(TYPES.NotificationService).to(NotificationService);
container.bind(TYPES.AuthController).to(AuthController);
container.bind(TYPES.TicketController).to(TicketController);
export default container;
